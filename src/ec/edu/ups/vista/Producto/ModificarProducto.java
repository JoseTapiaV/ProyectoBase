    public void mostrarCategorias(CategoriaProducto cp) throws Exception {
        jComboBox3.addItem(cp.getId() + " " + cp.getNombre());
        Set<CategoriaProducto> list = categoriaControlador.listaCategoria();
        for (CategoriaProducto c : list) {
            if (cp.getId() != c.getId()) {
                jComboBox3.addItem(c.getId() + " " + c.getNombre());
            }
        }
    }

    public void mostrarUnidades(UnidadMedida um) throws Exception {
        Set<UnidadMedida> list = unidadesControlador.listaUn();
        jComboBox7.addItem(um.getId() + " " + um.getNombre());
        for (UnidadMedida u : list) {
            if (um.getId() != u.getId()) {
                jComboBox7.addItem(u.getId() + " " + u.getNombre());
            }
        }
    }

    public void mostrarIVA(char c) {
        jComboBox4.addItem(String.valueOf(c));
        if (c == 'T') {
            jComboBox4.addItem("F");
        } else {
            jComboBox4.addItem("T");
        }
    }

    public void mostrarNaci(char c) {
        jComboBox2.addItem(String.valueOf(c));
        if (c == 'N') {
            jComboBox2.addItem("E");
        } else {
            jComboBox2.addItem("N");
        }
    }

