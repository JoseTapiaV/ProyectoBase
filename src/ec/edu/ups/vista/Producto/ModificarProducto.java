    private void jToggleButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton3ActionPerformed
        jComboBox2.removeAllItems();
        jComboBox3.removeAllItems();
        jComboBox7.removeAllItems();
        jComboBox4.removeAllItems();
        Producto p = controladorProducto.Buscar(Integer.parseInt(jTId.getText()));
        stock = p.getId();
        jTCBarra.setText(p.getCodBarra());
        jTNombre.setText(p.getNombre());
        jTPrecio.setText(String.valueOf(p.getPrecio()));
        mostrarIVA(p.getIva());
        mostrarNaci(p.getNacionalidad());
        try {
            mostrarUnidades(p.getUm());
            mostrarCategorias(p.getCp());
        } catch (Exception ex) {
            System.out.println("Mostrsr unidades");;
        }
    }//GEN-LAST:event_jToggleButton3ActionPerformed

    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        Producto p = new Producto();
        p.setId(Integer.parseInt(jTId.getText()));
        p.setCodBarra(jTCBarra.getText());
        p.setNombre(jTNombre.getText());
        p.setPrecio(Double.parseDouble(jTPrecio.getText()));
        p.setNacionalidad(jComboBox2.getSelectedItem().toString().charAt(0));
        p.setIva(jComboBox4.getSelectedItem().toString().charAt(0));
        p.setUm(unidadesControlador.Buscaar(Integer.parseInt(jComboBox7.getSelectedItem().toString().substring(0,1))));
        p.setCp(categoriaControlador.Buscar(Integer.parseInt(jComboBox3.getSelectedItem().toString().substring(0,1))));
        p.setStock(stock);      
        try {
            controladorProducto.modificar(p);
        } catch (SQLException ex) {
            System.out.println("Modificar Producto");
        }
        JOptionPane.showMessageDialog(this, "Producto creado correctamente", "Crear Producto", JOptionPane.OK_OPTION);
        jTId.setText("");
        jTCBarra.setText("");
        jTNombre.setText("");
        jTPrecio.setText("");
        jComboBox2.removeAllItems();
        jComboBox3.removeAllItems();
        jComboBox7.removeAllItems();
        jComboBox4.removeAllItems();
    }//GEN-LAST:event_jToggleButton1ActionPerformed


