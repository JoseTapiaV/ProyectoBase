    private void tblServFKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblServFKeyReleased

       int key = evt.getKeyCode();
        if (key == KeyEvent.VK_ENTER) {
            int fila = tblServF.getSelectedRow();
            int columna = tblServF.getSelectedColumn();
            int codigoServ = 0;

            Object[] datos1 = {"", 0, "", "", "", "", ""};

            if (columna == 0) {
                codigoServ = Integer.parseInt(tblServF.getValueAt(fila, columna).toString());
                pro = contPro.Buscar(codigoServ);

                int cant = Integer.parseInt(tblServF.getValueAt(fila, 1).toString());

                tablaF = (DefaultTableModel) tblServF.getModel();
                tablaF.removeRow(fila);

                if (pro.getIva() == 'C') {

                    a = cant * pro.getPrecio();
                    ivaPro = a * 0.12;
                    totalCP = a + ivaPro;

                    Object[] datos = {codigoServ,
                        cant,
                        pro.getNombre(),
                        pro.getPrecio(),
                        ivaPro,
                        totalCP
                    };

                    tablaF.addRow(datos);
                    tablaF.addRow(datos1);

                } else if (pro.getIva() == 'S') {

                    ivaPro = 0;

                    totalCP = cant * pro.getPrecio();

                    Object[] datos = {codigoServ,
                        cant,
                        pro.getNombre(),
                        pro.getPrecio(),
                        ivaPro,
                        totalCP
                    };

                    tablaF.addRow(datos);
                    tablaF.addRow(datos1);
                }

            } else if (columna == 1) {

                codigoServ = Integer.parseInt(tblServF.getValueAt(fila, columna - 1).toString());
                int cant = Integer.parseInt(tblServF.getValueAt(fila, columna).toString());
                tablaF.removeRow(fila);
                tablaF.removeRow(tblServF.getRowCount() - 1);

                if (pro.getIva() == 'C') {

                    a = cant * pro.getPrecio();
                    ivaPro = a * 0.12;
                    totalCP = a + ivaPro;

                    Object[] datos = {codigoServ,
                        cant,
                        pro.getNombre(),
                        pro.getPrecio(),
                        ivaPro,
                        totalCP
                    };

                    tablaF.addRow(datos);
                    tablaF.addRow(datos1);

                } else if (pro.getIva() == 'S') {

                    ivaPro = 0;
                    totalCP = cant * pro.getPrecio();

                    Object[] datos = {codigoServ,
                        cant,
                        pro.getNombre(),
                        pro.getPrecio(),
                        ivaPro,
                        totalCP
                    };

                    tablaF.addRow(datos);
                    tablaF.addRow(datos1);
                }

            }

            subtotal = subtotal + totalCP;
            txtSubtotal.setText(String.valueOf(subtotal));

            iva = subtotal * 0.12;
            txtIva.setText(String.valueOf(iva));

            total = subtotal + iva;
            txtTotal.setText(String.valueOf(total));

            float pv = 0, pc = 0, pg;
            pv = Float.parseFloat(txtSubtotal.getText());
            pc = Float.parseFloat(txtIva.getText());
            pg = Float.parseFloat(txtTotal.getText());

            txtSubtotal.setText(String.format(Locale.US, "%.2f", pv));
            txtIva.setText(String.format(Locale.US, "%.2f", pc));
            txtTotal.setText(String.format(Locale.US, "%.2f", pg));

        }

    }//GEN-LAST:event_tblServFKeyReleased

